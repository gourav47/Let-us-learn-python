'''understanding while loop'''
x=1                   #initialization
while x<=5:           #condition to get the output repeatedly
      print('Gourav')
      x=x+1           #flow(deciding statement for flow of the output

#These three are important factor in while loop i.e; initialization, condition and flow

'''another example for while loop: print first 10 natural numbers'''
x=1              #initialization
while x<=10:     #condition
    print(x)
    x=x+1        #flow

'''printing natural numbers in reverse'''
x=10
while x>=1:
    print(x)
    x=x-1
    
      
