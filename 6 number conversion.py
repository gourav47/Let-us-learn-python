''' program to shopw the number system conversion'''
x=0b10101  #binary to decimal
print(x)
x=0B10101  #binary to decimal(another representation)
print(x)

y=0B1110101 #binary to decimal with another example
print(y)
y=0b1110101
print(y)

z=0o165     #octal to decimal conversion
print(z)
z=0O165
print(z)

z=0o334   #octal to decimal conversion with another example
print(z)
z=0O334
print(z)


w=0x21c   #hexa decimal to decimal conversion
print(w)
w=0X21c
print(w)

v=0x16    #hexadecimal to decimal conversion with another example.
print(v)

'''python by default convert any form of numbers into decimal'''

